    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable2.getSelectedRow();
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diedit!", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    tableModel.setValueAt(jTextField1.getText(), selectedRow, 0);
    tableModel.setValueAt(jTextField2.getText(), selectedRow, 1);
    tableModel.setValueAt(new java.text.SimpleDateFormat("dd/MM/yyyy").format(jDateChooser1.getDate()), selectedRow, 2);
    tableModel.setValueAt(jTextField3.getText(), selectedRow, 3); // Update kategori
    clearInput();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String agenda = jTextField1.getText();
    String waktu = jTextField2.getText();
    java.util.Date tanggal = jDateChooser1.getDate();
    String kategori = jTextField3.getText(); // Ambil input kategori

    if (agenda.isEmpty() || waktu.isEmpty() || tanggal == null || kategori.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
    } else {
        tableModel.addRow(new Object[]{
            agenda, 
            waktu, 
            new java.text.SimpleDateFormat("dd/MM/yyyy").format(tanggal), 
            kategori
        });
        clearInput();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

